# ============================================================
# 统一获取接口
# ============================================================
def get_electrode_coords(dataset_name, num_electrodes=None):
    """
    根据数据集名称获取电极坐标
    
    Args:
        dataset_name: 数据集名称 ('seed', 'seed-iv', 'deap', 'dreamer')
        num_electrodes: 电极数量（可选，用于验证）
    
    Returns:
        coords: (N, 3) numpy array，电极3D坐标
        names: list[str]，电极名称
        regions: list[str]，电极所属脑区（可选）
    """
    dataset_name = dataset_name.lower().replace("_", "-")
    
    if dataset_name in ["seed", "seed-iv", "seediv"]:
        coords = SEED_62_COORDS
        names = SEED_62_NAMES
        regions = None
        
        if num_electrodes is not None and num_electrodes != 62:
            raise ValueError(f"SEED/SEED-IV should have 62 electrodes, got {num_electrodes}")
    
    elif dataset_name == "deap":
        coords = DEAP_32_COORDS
        names = DEAP_32_NAMES
        regions = DEAP_32_REGIONS
        
        if num_electrodes is not None and num_electrodes != 32:
            raise ValueError(f"DEAP should have 32 electrodes, got {num_electrodes}")
    
    else:
        raise ValueError(f"Unsupported dataset: {dataset_name}")
    
    return coords, names, regions
